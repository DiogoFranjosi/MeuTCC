        <h2>OBRAS</h2>
 
<p>Brás Cubas, um dos principais personagens de Machado de Assis, afirmou, no final de suas Memórias Póstumas:</p>
 
<p>“Não tem filhos, não transmite a nenhuma criatura o legado de nossa miséria.”</p>
 
<p>Da mesma forma, o autor também não teve filhos. Entretanto, Machado deixou, como legado, uma obra de valor incalculável. Algumas de suas publicações foram:
</p>
<br>
 
  <ul>
       <li>Ressurreição (1872)</li>
       <li>Histórias da meia-noite (1873)</li>
       <li>A Mão e a Luva (1874)</li>
       <li>Iaiá Garcia (1878)</li>
       <li>Memórias Póstumas de Brás Cubas (1881)</li>
       <li>Papéis Avulsos (1882)</li>
       <li>O Alienista (1882)</li>
       <li>Quincas Borba (1891)</li>
       <li>Dom Casmurro (1899)</li>
       <li>Esaú e Jacó (1904);</li>
       <li>Memorial de Aires (1908)</li>
   </ul>
 
 
